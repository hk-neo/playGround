a("### Functional Requirements (기능 요구사항)")
a("")
a("- **FR-001**: 시스템은 DICOM 메타데이터 객체에서 PHI 필드를 `[REDACTED]`로 마스킹하는 `maskPhiFields(metadata)` 함수를 제공해야 한다. 함수 시그니처: `maskPhiFields(metadata: Object|null|undefined) -> Object|null|undefined` (추적: FR-4.1, HAZ-3.1)")
a("- **FR-002**: 시스템은 PHI 마스킹 대상 필드를 `PHI_FIELDS` 상수 배열로 관리해야 한다. `PHI_FIELDS = ['patientName', 'patientID', 'patientBirthDate']`로, DICOM Tag (0010,0010), (0010,0020), (0010,0030)에 해당한다. (추적: FR-4.1, SEC-3)")
a("- **FR-003**: 시스템은 마스킹 문자열을 `PHI_MASK` 상수로 관리해야 한다. `PHI_MASK = '[REDACTED]'`. 향후 마스킹 정책 변경 시 단일 지점 수정으로 대응 가능해야 한다.")
a("- **FR-004**: maskPhiFields()는 metadata가 null, undefined, 또는 객체가 아닌 경우 입력값을 그대로 반환하고 예외를 발생시키지 않아야 한다. (추적: NFR-4)")
a("- **FR-005**: maskPhiFields()는 PHI 필드 값이 undefined이거나 빈 문자열(``)인 경우 마스킹을 생략해야 한다. (추적: FR-4.1)")
a("- **FR-006**: maskPhiFields()는 유효한 PHI 필드 값을 `[REDACTED]`로 치환하고, 원본 값을 phiStore(WeakMap)에 metadata 객체 참조를 키로 하여 저장해야 한다. (추적: FR-4.1, HAZ-3.1)")

a("- **FR-007**: 시스템은 마스킹된 메타데이터에서 원본 PHI 값을 안전하게 조회하는 `getPhiValue(metadata, field)` 함수를 제공해야 한다. 함수 시그니처: `getPhiValue(metadata: Object, field: string) -> string|undefined` (추적: FR-4.1)")
a("- **FR-008**: getPhiValue()는 field가 PHI_FIELDS에 포함되지 않은 경우 undefined를 반환하여 비인가 필드 조회를 차단해야 한다. (추적: SEC-3)")
a("- **FR-009**: getPhiValue()는 phiStore에 해당 metadata 키가 존재하지 않는 경우 undefined를 반환해야 한다. (추적: FR-4.1)")
a("- **FR-010**: 시스템은 모든 원본 PHI 값을 일괄 조회하는 `dumpPhiValues(metadata)` 함수를 제공해야 한다. 함수 시그니처: `dumpPhiValues(metadata: Object) -> Object`. 이 함수는 @internal로 분류되어 배럴 파일(index.js)에서 export되지 않는다.")
a("- **FR-011**: phiStore는 모듈 스코프의 `WeakMap<Object, Object>`이며, 외부에서 직접 접근할 수 없다. 원본 PHI 값의 유일한 저장소다.")
a("- **FR-012**: maskPhiFields()는 동일한 metadata 참조를 반환해야 한다 (새 객체 생성 없음). 이는 metadataParser.js에서 반환값 없이 사용할 수 있도록 한다.")

