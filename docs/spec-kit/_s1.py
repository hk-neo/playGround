a("### Non-Functional Requirements (비기능 요구사항)")
a("")
a("- **NFR-001 (보안)**: PHI 원본 값은 phiStore(WeakMap)에만 저장되며, 모듈 외부에서 직접 접근할 수 없어야 한다. 원본 조회는 getPhiValue() 함수를 통해서만 가능하다. (추적: SEC-3, HAZ-3.1)")
a("- **NFR-002 (메모리 안전성)**: WeakMap을 사용하여 metadata 객체가 가비지 컬렉션될 때 원본 PHI 값도 자동으로 해제되어 메모리 누수가 발생하지 않아야 한다.")
a("- **NFR-003 (불변성)**: PHI_FIELDS 배열과 PHI_MASK 상수는 `const`로 선언되어 런타임 수정이 불가능해야 한다.")
a("- **NFR-004 (견고성)**: maskPhiFields()는 null, undefined, 비객체 입력에 대해 예외를 발생시키지 않고 안전하게 처리해야 한다. (추적: IEC 62304 Class A)")
a("- **NFR-005 (단일 책임)**: phiGuard.js는 PHI 마스킹과 원본 값 안전 보관만을 담당하며, 비즈니스 로직을 포함하지 않아야 한다. (IEC 62304 Class A)")
